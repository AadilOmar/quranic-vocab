const tabs = [
  { href: "/", label: "Read", Icon: ReadIcon },
  { href: "/lists", label: "Lists", Icon: ListIcon },
  { href: "/settings", label: "Settings", Icon: SettingsIcon },
];

export default function BottomNav() {
  const pathname = usePathname();

  return (
    <nav
      className="fixed bottom-0 left-0 right-0 bg-white border-t border-stone-100 flex items-center justify-around z-50"
      style={{ paddingBottom: "env(safe-area-inset-bottom)" }}
    >
      {tabs.map(({ href, label, Icon }) => {
        const isActive = href === "/" ? pathname === "/" : pathname.startsWith(href);
        return (
          <Link
            key={href}
            href={href}
            className={`flex flex-col items-center gap-1 px-8 py-3 transition-colors ${
              isActive ? "text-amber-500" : "text-stone-400"
            }`}
          >
            <Icon active={isActive} />
            <span className="text-xs font-medium">{label}</span>
          </Link>
        );
      })}
    </nav>
  );
}
